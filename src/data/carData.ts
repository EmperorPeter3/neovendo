// Generate year options (from current year to 1970)
export const getYearOptions = (): number[] => {
  const currentYear = new Date().getFullYear();
  const years: number[] = [];
  for (let year = currentYear; year >= 1970; year--) {
    years.push(year);
  }
  return years;
};
